# prototype/eval_paired_runner.py — P0 first-move advantage measurement runner
#
# Runs all experiments A2 (three-scenario calibration) and A3 (P0 effect decomposition).
# Uses eval_matrix.py with --paired flag and --mode flag.
#
# Usage: python -m prototype.eval_paired_runner

import argparse, json, os, sys, math, subprocess, time, datetime

# Add project root to path
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

OUTPUT_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "eval_paired")


def _ci(p, n):
    """95% CI width for a proportion"""
    if n < 1:
        return 0.0
    return 1.96 * math.sqrt(p * (1 - p) / n)


def _mean_std(values):
    """Return (mean, stddev) for a list of values."""
    n = len(values)
    if n < 2:
        return (sum(values) / n if n else 0.0, 0.0)
    m = sum(values) / n
    v = sum((x - m) ** 2 for x in values) / (n - 1)
    return (m, math.sqrt(v))


def run_scenario(name, size, city_hp=None, city_damage=None,
                 games=500, ais="greedy,greedy",
                 gen="balanced", max_turns=100, workers=None,
                 seed=42, mode="normal"):
    """Run a single scenario using --city-hp/--city-damage CLI flags."""
    output_dir = os.path.join(OUTPUT_DIR, name)
    os.makedirs(output_dir, exist_ok=True)

    cmd = [
        sys.executable, "-m", "prototype.eval_matrix",
        "--paired",
        "--ais", ais,
        "--games", str(games),
        "--size", str(size),
        "--workers", str(workers or max(1, os.cpu_count() or 4)),
        "--output", output_dir,
        "--seed", str(seed),
        "--max-turns", str(max_turns),
        "--gen", gen,
    ]
    if mode != "normal":
        cmd.extend(["--mode", mode])
    if city_hp is not None:
        cmd.extend(["--city-hp", str(city_hp)])
    if city_damage is not None:
        cmd.extend(["--city-damage", str(city_damage)])

    print(f"\n{'='*60}")
    config_str = f"size={size}"
    if city_hp is not None:
        config_str += f", CITY_HP={city_hp}"
    if city_damage is not None:
        config_str += f", CITY_DAMAGE={city_damage}"
    print(f"Scenario: {name} ({config_str}, mode={mode})")
    print(f"{'='*60}")
    sys.stdout.flush()

    result = subprocess.run(cmd, capture_output=True, text=True)
    print(result.stdout)
    if result.stderr:
        stderr_short = result.stderr[:1000]
        if stderr_short.strip():
            print(f"STDERR: {stderr_short}")

    # Load results
    summary_path = os.path.join(output_dir, "summary.json")
    if os.path.exists(summary_path):
        with open(summary_path) as f:
            return json.load(f)
    else:
        print(f"WARNING: {summary_path} not found!")
        return None


def run_scenario_adaptive(name, size, city_hp, city_damage,
                          initial_games=500, ais="greedy,greedy",
                          gen="balanced", max_turns=100, workers=None,
                          seed=42, max_std=0.03):
    """Run scenario with adaptive sample size until P0 winrate std < max_std (CI width < 6%)."""
    games = initial_games

    while True:
        result = run_scenario(name, size, city_hp, city_damage,
                              games=games, ais=ais, gen=gen,
                              max_turns=max_turns, workers=workers, seed=seed)
        if result is None or not result.get("pairs"):
            print(f"  ERROR: No results for {name}")
            return None

        pair_data = result["pairs"][0]
        p0_std = pair_data.get("p0_std", 1.0)

        print(f"  P0 winrate stddev: {p0_std*100:.2f}% (target < {max_std*100:.2f}%)")

        if p0_std < max_std:
            return result
        else:
            games *= 2
            print(f"  Stddev too high ({p0_std*100:.2f}%). Doubling sample size to {games} seeds...")


def _load_summary(name):
    """Load summary.json from a scenario subdirectory."""
    summary_path = os.path.join(OUTPUT_DIR, name, "summary.json")
    if os.path.exists(summary_path):
        with open(summary_path) as f:
            return json.load(f)
    return None


